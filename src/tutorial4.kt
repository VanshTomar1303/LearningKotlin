// Operators
/*

    1. Arithmetic Operators (+,-,/,*,%)
    2. Relational Operators(>,<,==,>=,<--,!=)
    3. Increment and Decrement Operator (++,--)

 */
fun main(){
    var a=10
    var b=30
    println(a+b)
    println(a-b)
    println(a*b)
    println(b/a)


    // post increment and decrement
    println(b++)
    println(a--)
    // pre increment and decrement
    println(++b)
    println(--a)


    println(a>b)
    println(a<b)
    println(a>=b)
    println(a<=b)
    println(a==b)
    println(a!=b)
}